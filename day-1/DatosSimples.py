# Strings

#   Las comillas dobles y las comillas simples solo nos permiten escribir en una sola linea de codigo, si intentamos escribir sobre el mismo string mas de una linea, Obtendremos un error.

"Hola mundo" #<--- Comillas dobles
'Hola Mundo' #<--- Comillas simples

#Para escribir sobre un string en varias lineas de codigo debemos usar las triples comillas. Esto es similar a los backtick de Javascript


#Triples comillas dobles
"""
    nombre: Samuel,
    Apellido: Vasquez
"""

#Triples comillas simples
'''
    nombre: Samuel,
    Apellido: Vasquez
'''

